// ===========================================================
//                     Class Definition
// ===========================================================
class Circle {
  constructor(radius) {
    this.radius = radius;
  }

  // ===========================================================
  //                    Static Method
  // ===========================================================
  static parse(str) {
    const radius = JSON.parse(str).radius;
    return new Circle(radius);
  }

  // ===========================================================
  //                    Instance Method
  // ===========================================================
  draw() {}
}

// ===========================================================
//                    Calling Instance Method
// ===========================================================
const circle = new Circle(1);

// ===========================================================
//                    Calling Static Method
// ===========================================================
console.log(Circle.parse('{"radius":1}'));
