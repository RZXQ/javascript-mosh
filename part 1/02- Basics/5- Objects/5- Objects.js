// Reference types in JavaScript: Object, Array, Function

// Object literal
const person = { name: "Ruizhi", age: 33 };
console.log(person);

// 1. Dot notation:
person.name = "DOT";
console.log(person);

// 2. Bracket notation:
let selection = "property";
person[selection] = "BRACKET";
console.log(person);
