// Symbols in JavaScript:
// ;  -> Semicolon
// ,  -> Comma
// :  -> Colon
// () -> Parentheses
// []  -> Square Brackets
// {}  -> Curly Braces
// <>  -> Angle Brackets

// 1. Performing a task
function greet(firstName, lastName) {
  console.log("Hello " + firstName + " " + lastName);
}
greet("Reacher", "WANG");

// 2. Calculating a value
function square(number) {
  return number * number;
}
console.log(square(2));
