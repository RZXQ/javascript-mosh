// ===========================================================
//                        Variable Declarations
// ===========================================================
// Note: From ES6+, we use 'let' instead of 'var'.

let name1;
console.log(name1); // undefined

let name2 = "Reacher";
console.log(name2);

// ===========================================================
//                      Naming Conventions
// ===========================================================
// - Cannot be a reserved keyword.
// - Should be meaningful.
// - Cannot start with a number (e.g., 1name).
// - Cannot contain a space or hyphen (-).
// - Are case-sensitive.

// ===========================================================
//                    Declaration Examples
// ===========================================================

let firstName; // Camel case notation
let FirstName; // These variable names are different
