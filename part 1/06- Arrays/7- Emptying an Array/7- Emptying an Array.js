// ===========================================================
//                           numbers=[]
// ===========================================================

let numbers = [1, 2, 3, 4];
numbers = [];
console.log(numbers); // []

// ===========================================================
//                      numbers.length = 0
// ===========================================================

numbers = [1, 2, 3, 4];
numbers.length = 0;
console.log(numbers); // []

// ===========================================================
//                numbers.splice(0, numbers.length)
// ===========================================================

numbers = [1, 2, 3, 4];
numbers.splice(0, numbers.length);
console.log(numbers); // []

// ===========================================================
//                while (numbers.length > 0) numbers.pop()
// ===========================================================

numbers = [1, 2, 3, 4];
while (numbers.length > 0) numbers.pop();
console.log(numbers); // []
