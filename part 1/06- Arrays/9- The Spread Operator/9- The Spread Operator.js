// ===========================================================

const first = [1, 2, 3];
const second = [4, 5, 6];

// ===========================================================
//                    Combine Arrays
// ===========================================================

const combined1 = [...first, ...second];
console.log(combined1); // [1, 2, 3, 4, 5, 6]

// ===========================================================
//          Add Custom Elements While Combining
// ===========================================================

const combined2 = [...first, { id: 1 }, ...second];
console.log(combined2); // [1, 2, 3, { id: 1 }, 4, 5, 6]

// ===========================================================
//                 Create a Shallow Copy
// ===========================================================

const copy = [...combined1];
console.log(copy); // [1, 2, 3, 4, 5, 6]
